# include <stdio.h>

int main(int argc, char const *argv[])
{
    float f1 = 7.625f, f2 = 1.3f, f3 = -7.625f;
    double d1 = 7.625, d2 = 1.3, d3 = -7.625;  
    printf("f1 = %f\n", f1);
    printf("f2 = %f\n", f2);
    printf("f3 = %f\n", f3);
    printf("d1 = %lf\n", d1);
    printf("d2 = %lf\n", d2);
    printf("d3 = %lf\n", d3);

    return 0;
}